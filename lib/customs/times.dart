class Times {
  int start = 5;
  int seconds = 0;
  int minutes = 0;
  int hours = 0;

  int getStart() {
    return start;
  }

  void setStart(int start) {
    this.start = start;
  }

  int getSeconds() {
    return seconds;
  }

  void setSeconds(int seconds) {
    this.seconds = seconds;
  }

  int getMinutes() {
    return minutes;
  }

  void setMinutes(int minutes) {
    this.minutes = minutes;
  }

  int getHours() {
    return hours;
  }

  void setHours(int hours) {
    this.hours = hours;
  }
}
